#include<bits/stdc++.h>
using namespace std;
auto main()->int{
    short int shortInt;
    int numInt;
    long int longInt;
    long long longlongInt;
    unsigned long long int unsignedLongLongInt;
    float numFloat;
    double numDouble;
    long double longDouble;
    char letter;
    cout<<"short int "<<sizeof(shortInt)<<endl;
    cout<<"int "<<sizeof(numInt)<<endl;
    cout<<"long int "<<sizeof(longInt)<<endl;
    cout<<"long long int "<<sizeof(longlongInt)<<endl;
    cout<<"unsigned long long int "<<sizeof(unsignedLongLongInt)<<endl;
    cout<<"float "<<sizeof(numFloat)<<endl;
    cout<<"double "<<sizeof(numDouble)<<endl;
    cout<<"long double "<<sizeof(longDouble)<<endl;
    cout<<"char "<<sizeof(letter)<<endl;
}